function getLocation() {
  var loactions = [];

  for (var index in conData) {
    //console.log(index);
    var location1 = conData[index].place;
    if (loactions.indexOf(location1) < 0) {
      if (loactions.length == 0) {
        var option = document.createElement("option");
        option.innerHTML = "지역을 선택해주세요";
        option.setAttribute("value", "지역을 선택해주세요");
        document.getElementById("sel1").appendChild(option);
      }

      var option = document.createElement("option");

      option.innerHTML = location1;

      option.setAttribute("value", location1);

      document.getElementById("sel1").appendChild(option);

      loactions.push(location1);
    }
  }
}

